import math

n1 = int(input('Digite um número: '))
nd = n1 * 2
nt = n1 * 3
nq = math.sqrt(n1)
print('Analisando dados sobre o valor {0}... O dobro é {1}, seu triplo {2} e sua raiz quadrada {3}'.format(n1, nd, nt,
                                                                                                           nq))
